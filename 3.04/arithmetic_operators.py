x = 5
y = 8

# Addition
result = x + y
print(result, type(result))

# Subtraction
result = x - y
print(result, type(result))

# Multiplication 
result = x * y
print(result, type(result))

# Division
result = x / y
print(result, type(result))

# Floor Division
result = x // y
print(result, type(result))

# Modulus
result = x % y
print(result, type(result))

# Exponent
result = x ** y
print(result, type(result))